// 한국 공휴일(대략) - 필요 시 이 목록을 직접 수정하세요.
// 배송예정일 계산에 사용: 주말(토·일) + 아래 공휴일은 건너뜁니다.
const p2 = (n: number) => String(n).padStart(2, "0");
const toIso = (d: Date) => `${d.getFullYear()}-${p2(d.getMonth() + 1)}-${p2(d.getDate())}`;

export const KR_HOLIDAYS = new Set<string>([
  // 2024
  "2024-01-01","2024-02-09","2024-02-10","2024-02-11","2024-02-12","2024-03-01",
  "2024-04-10","2024-05-05","2024-05-06","2024-05-15","2024-06-06","2024-08-15",
  "2024-09-16","2024-09-17","2024-09-18","2024-10-01","2024-10-03","2024-10-09","2024-12-25",
  // 2025
  "2025-01-01","2025-01-27","2025-01-28","2025-01-29","2025-01-30","2025-03-01","2025-03-03",
  "2025-05-05","2025-05-06","2025-06-06","2025-08-15","2025-10-03","2025-10-05","2025-10-06",
  "2025-10-07","2025-10-08","2025-10-09","2025-12-25",
  // 2026
  "2026-01-01","2026-02-16","2026-02-17","2026-02-18","2026-03-01","2026-03-02","2026-05-05",
  "2026-05-24","2026-05-25","2026-06-06","2026-08-15","2026-08-17","2026-09-24","2026-09-25",
  "2026-09-26","2026-09-28","2026-10-03","2026-10-05","2026-10-09","2026-12-25",
]);

export function isBusinessDay(d: Date): boolean {
  const g = d.getDay(); if (g === 0 || g === 6) return false;
  return !KR_HOLIDAYS.has(toIso(d));
}

// 주어진 날짜의 "다음 영업일"(주말·공휴일 건너뜀)
export function nextBusinessDay(iso: string): string {
  if (!iso) return "";
  const d = new Date(iso + "T00:00:00");
  d.setDate(d.getDate() + 1);
  let guard = 0;
  while (!isBusinessDay(d) && guard < 40) { d.setDate(d.getDate() + 1); guard++; }
  return toIso(d);
}
